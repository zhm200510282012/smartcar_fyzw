#error STEERING_RIGHT_SIGN must be +1 or -1.
#endif

#if (FUZZY_ENABLE != 0) && (FUZZY_ENABLE != 1)
#error FUZZY_ENABLE must be 0 or 1.
#endif

#if (POWER_GUARD_ENABLE != 0) && (POWER_GUARD_ENABLE != 1)
#error POWER_GUARD_ENABLE must be 0 or 1.
#endif

#if (APP_ARM_SOURCE != APP_ARM_SOURCE_ALWAYS_FOR_BENCH) && \
    (APP_ARM_SOURCE != APP_ARM_SOURCE_GO_KEY) && \
    (APP_ARM_SOURCE != APP_ARM_SOURCE_UART)
#error APP_ARM_SOURCE must be one of APP_ARM_SOURCE_*.
#endif

#if (FAN_ESC_PHYSICAL_OUTPUT_ENABLE != 0) && (FAN_ESC_PHYSICAL_OUTPUT_ENABLE != 1)
#error FAN_ESC_PHYSICAL_OUTPUT_ENABLE must be 0 or 1.
#endif

#if (WALL_RUN_ENABLE != 0) && (WALL_RUN_ENABLE != 1)
#error WALL_RUN_ENABLE must be 0 or 1.
#endif

#if (ROUTE_EVENT_SOURCE_DEFAULT != ROUTE_EVENT_SOURCE_NONE) && \
    (ROUTE_EVENT_SOURCE_DEFAULT != ROUTE_EVENT_SOURCE_HOST_SIL) && \
    (ROUTE_EVENT_SOURCE_DEFAULT != ROUTE_EVENT_SOURCE_MANUAL_INJECT) && \
    (ROUTE_EVENT_SOURCE_DEFAULT != ROUTE_EVENT_SOURCE_PROGRESS_SCRIPT)
#error ROUTE_EVENT_SOURCE_DEFAULT must be one of the ROUTE_EVENT_SOURCE_* values.
#endif

#if (ROUTE_PROGRESS_SCRIPT_ENABLE != 0) && (ROUTE_PROGRESS_SCRIPT_ENABLE != 1)
#error ROUTE_PROGRESS_SCRIPT_ENABLE must be 0 or 1.
#endif

#if (IMU_PITCH_SIGN != 1) && (IMU_PITCH_SIGN != -1)
#error IMU_PITCH_SIGN must be +1 or -1.
#endif

#if (IMU_PITCH_AXIS > 2u)
#error IMU_PITCH_AXIS must be 0, 1, or 2.
#endif

#endif
