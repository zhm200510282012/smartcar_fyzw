#define SPEED_OUTPUT_LIMIT 260
#define SPEED_ACCEL_LIMIT 12

#define POWER_GUARD_ENABLE 0
#define POWER_ADC_CHANNEL 0u
#define POWER_ADC_FILTER_ALPHA 3u
#define POWER_ADC_FILTER_DENOM 4u
#define POWER_LOW_RAW_THRESHOLD 1500u
#define POWER_VOLTAGE_CALIBRATION_VALID 0
#define POWER_ADC_MV_PER_COUNT_NUM 0ul
#define POWER_ADC_MV_PER_COUNT_DEN 1ul

#define APP_ARM_SOURCE_ALWAYS_FOR_BENCH 0u
#define APP_ARM_SOURCE_GO_KEY 1u
#define APP_ARM_SOURCE_UART 2u
#define APP_ARM_SOURCE APP_ARM_SOURCE_ALWAYS_FOR_BENCH
#define APP_ARM_DEBOUNCE_MS 20u

#define ENCODER_COUNTS_PER_WHEEL_REV 0L
#define WHEEL_CIRCUMFERENCE_MM 0L
